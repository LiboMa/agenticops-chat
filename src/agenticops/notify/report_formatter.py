# ---------------------------------------------------------------------------
# Newsletter helper functions
# ---------------------------------------------------------------------------


def _parse_newsletter_items(body_html: str) -> str:
    """Post-process rendered HTML to convert metadata lines into styled badges.

    Patterns matched (case-insensitive, bilingual):
      - ``联系人：XXX`` / ``Contact: XXX`` → blue contact badge
      - ``公开范围：XXX`` / ``Confidentiality: XXX`` → green scope badge

    Handles both standalone ``<p>`` tags and lines merged by ``nl2br``
    (where consecutive lines share a ``<p>`` separated by ``<br />``).
    """
    # Contact badge — match text before <br> or </p>
    body_html = re.sub(
        r"(?:联系人|Contact)\s*[：:]\s*(.+?)(?=<br\s*/?>|</p>)",
        r'<span style="display:inline-block;background:#dbeafe;color:#1e40af;font-size:12px;padding:2px 10px;border-radius:12px;">&#128100; \1</span>',
        body_html,
        flags=re.IGNORECASE,
    )
    # Confidentiality / scope badge — match text before <br> or </p>
    body_html = re.sub(
        r"(?:公开范围|Confidentiality)\s*[：:]\s*(.+?)(?=<br\s*/?>|</p>)",
        r'<span style="display:inline-block;background:#dcfce7;color:#166534;font-size:12px;padding:2px 10px;border-radius:12px;">&#128274; \1</span>',
        body_html,
        flags=re.IGNORECASE,
    )
    return body_html


def _inject_section_ids(body_html: str) -> str:
    """Add ``id="section-N"`` to ``<h2>`` tags for TOC anchor links."""
    counter = 0

    def _add_id(match: re.Match) -> str:
        nonlocal counter
        counter += 1
        attrs = match.group(1) or ""
        return f'<h2 id="section-{counter}"{" " + attrs if attrs else ""}>'

    return re.sub(r"<h2(?:\s([^>]*))?>", _add_id, body_html)


def _build_newsletter_toc(body_html: str) -> str:
    """Build a numbered TOC box from ``<h2 id="section-N">`` tags.

    Returns empty string if fewer than 2 sections found.
    """
    headings = re.findall(r'<h2 id="section-(\d+)"[^>]*>(.*?)</h2>', body_html, re.DOTALL)
    if len(headings) < 2:
        return ""

    items = []
    for idx, (section_id, text) in enumerate(headings, 1):
        clean = re.sub(r"<[^>]+>", "", text).strip()
        items.append(
            f'<tr><td style="padding:3px 0;font-size:13px;color:#475569;">'
            f'{idx}. <a href="#section-{section_id}" style="color:#4f46e5;text-decoration:none;">{clean}</a>'
            f"</td></tr>"
        )

    return (
        '<tr><td style="padding:16px 32px 0;">'
        '<table role="presentation" width="100%" cellpadding="0" cellspacing="0" '
        'style="background:#f8fafc;border:1px solid #e2e8f0;border-radius:6px;padding:12px 16px;">'
        '<tr><td style="font-size:12px;font-weight:700;color:#64748b;text-transform:uppercase;'
        'letter-spacing:0.05em;padding-bottom:6px;">Contents</td></tr>'
        + "".join(items)
        + "</table></td></tr>"
    )


def _format_contributors(contributors: list) -> str:
    """Render a contributors band. Returns empty string if no contributors."""
    if not contributors:
        return ""
    names = ", ".join(str(c) for c in contributors)
    return (
        '<tr><td style="background:#fefce8;padding:12px 32px;font-size:13px;color:#854d0e;'
        'border-top:1px solid #e2e8f0;">'
        f"<strong>Contributors:</strong> {names}"
        "</td></tr>"
    )


def _to_newsletter_html(
    title: str,
    content: str,
    report_metadata: Dict,
) -> FormattedReport:
    """Render a newsletter-style HTML report.

    ``report_metadata`` may contain:
      - ``issue_number`` (int/str) — e.g. 102
      - ``classification`` (str) — e.g. "Amazon Confidential"
      - ``subtitle`` (str) — e.g. "GenAI Weekly Digest"
      - ``contributors`` (list[str]) — contributor names
    """
    if not _HAS_MARKDOWN:
        return FormattedReport(
            format="html",
            content=f"<html><body><pre>{content}</pre></body></html>".encode("utf-8"),
            content_type="text/html; charset=utf-8",
            extension=".html",
        )

    body_html = _md.markdown(
        content,
        extensions=["tables", "fenced_code", "toc", "nl2br"],
    )

    # Post-process metadata badges
    body_html = _parse_newsletter_items(body_html)

    # Inject section IDs and build TOC
    body_html = _inject_section_ids(body_html)
    toc_html = _build_newsletter_toc(body_html)

    # Inline styles on common tags for email compatibility
    body_html = body_html.replace(
        "<h2", '<h2 style="color:#1e293b;font-size:18px;margin:28px 0 12px;'
        'padding-bottom:6px;border-bottom:2px solid #e2e8f0;"'
    )
    body_html = body_html.replace(
        "<h3", '<h3 style="color:#334155;font-size:15px;margin:20px 0 8px;"'
    )
    body_html = body_html.replace(
        "<table>", '<table style="border-collapse:collapse;width:100%;margin:12px 0;font-size:14px;">'
    )
    body_html = re.sub(
        r"<th(?=[>\s])",
        '<th style="background:#f1f5f9;border:1px solid #cbd5e1;padding:6px 10px;'
        'text-align:left;font-weight:600;font-size:13px;"',
        body_html,
    )
    body_html = re.sub(
        r"<td(?=[>\s])",
        '<td style="border:1px solid #cbd5e1;padding:6px 10px;font-size:13px;"',
        body_html,
    )

    # Metadata from report_metadata
    issue_number = report_metadata.get("issue_number", "")
    classification = report_metadata.get("classification", "")
    subtitle = report_metadata.get("subtitle", "")
    contributors = report_metadata.get("contributors", [])

    issue_label = f"Issue NO.{issue_number}" if issue_number else ""
    classification_bg = "#fbbf24" if classification else "transparent"
    classification_fg = "#78350f" if classification else "transparent"

    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    contributors_html = _format_contributors(contributors)

    full_html = _NEWSLETTER_HTML_TEMPLATE.format(
        title=title,
        subtitle=subtitle,
        classification=classification,
        classification_bg=classification_bg,
        classification_fg=classification_fg,
        issue_label=issue_label,
        date=now,
        toc_html=toc_html,
        body_html=body_html,
        contributors_html=contributors_html,
    )

    return FormattedReport(
        format="html",
        content=full_html.encode("utf-8"),
        content_type="text/html; charset=utf-8",
        extension=".html",
    )


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def format_report(
    title: str,
    content_markdown: str,
    formats: list[str],
    report_metadata: Optional[dict] = None,
) -> list[FormattedReport]:
    """Convert a markdown report to the requested formats.

    Skips unavailable formats with a warning log instead of raising.

    Args:
        title: Report title.
        content_markdown: Full report content in Markdown.
        formats: List of desired formats, e.g. ``["html", "pdf", "markdown"]``.
        report_metadata: Optional metadata dict (report_type, etc.).

    Returns:
        List of ``FormattedReport`` objects for each successfully generated format.
    """
    meta = report_metadata or {}
    report_type = meta.get("report_type", "report")
    results: list[FormattedReport] = []

    for fmt in formats:
        fmt = fmt.lower().strip()
        try:
            if fmt == "markdown":
                results.append(_to_markdown(content_markdown))
            elif fmt == "html":
                if report_type == "newsletter":
                    results.append(_to_newsletter_html(title, content_markdown, meta))
                else:
                    results.append(_to_html(title, content_markdown, report_type))
            elif fmt == "pdf":
                result = _to_pdf(title, content_markdown, report_type)
                if result:
                    results.append(result)
            elif fmt == "docx":
                result = _to_docx(title, content_markdown, report_type)
                if result:
                    results.append(result)
            else:
                logger.warning("Unknown report format requested: %s", fmt)
        except Exception:
            logger.warning("Failed to generate %s format", fmt, exc_info=True)

    return results


# ---------------------------------------------------------------------------
# Format converters
# ---------------------------------------------------------------------------


def _to_markdown(content: str) -> FormattedReport:
    return FormattedReport(
        format="markdown",
        content=content.encode("utf-8"),
        content_type="text/markdown; charset=utf-8",
        extension=".md",
    )


def _to_html(title: str, content: str, report_type: str) -> FormattedReport:
    if not _HAS_MARKDOWN:
        logger.warning("'markdown' package not installed — HTML generation skipped")
        return FormattedReport(
            format="html",
            content=f"<html><body><pre>{content}</pre></body></html>".encode("utf-8"),
            content_type="text/html; charset=utf-8",
            extension=".html",
        )

    body_html = _md.markdown(
        content,
        extensions=["tables", "fenced_code", "toc", "nl2br"],
    )
    now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    full_html = _HTML_TEMPLATE.format(
        title=title,
        report_type=report_type,
        date=now,
        body_html=body_html,
    )
    return FormattedReport(
        format="html",
        content=full_html.encode("utf-8"),
        content_type="text/html; charset=utf-8",
        extension=".html",
    )


def _to_pdf(title: str, content: str, report_type: str) -> Optional[FormattedReport]:
    if not _HAS_WEASYPRINT:
        logger.warning("weasyprint not installed — PDF generation skipped (pip install 'agenticops[reports]')")
        return None

    import weasyprint

    html_report = _to_html(title, content, report_type)
    pdf_bytes = weasyprint.HTML(string=html_report.content.decode("utf-8")).write_pdf()
    return FormattedReport(
        format="pdf",
        content=pdf_bytes,
        content_type="application/pdf",
        extension=".pdf",
    )


def _to_docx(title: str, content: str, report_type: str) -> Optional[FormattedReport]:
    if not _HAS_DOCX:
        logger.warning("python-docx not installed — DOCX generation skipped (pip install 'agenticops[reports]')")
        return None

    from docx import Document
    from docx.shared import Pt
    from io import BytesIO

    doc = Document()

    # Title
    doc.add_heading(title, level=0)
    doc.add_paragraph(
        f"Type: {report_type}  |  Generated: {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}"
    ).style = doc.styles["Subtitle"] if "Subtitle" in [s.name for s in doc.styles] else None
    doc.add_paragraph("")  # spacer

    # Parse markdown into paragraphs — simple heuristic parser
    for line in content.split("\n"):
        stripped = line.strip()
        if not stripped:
            doc.add_paragraph("")
            continue

        # Headings
        heading_match = re.match(r"^(#{1,4})\s+(.*)", stripped)
        if heading_match:
            level = len(heading_match.group(1))
            doc.add_heading(heading_match.group(2), level=level)
            continue

        # Bullet points
        if stripped.startswith("- ") or stripped.startswith("* "):
            p = doc.add_paragraph(stripped[2:], style="List Bullet")
            continue

        # Numbered list
        numbered = re.match(r"^\d+\.\s+(.*)", stripped)
        if numbered:
            doc.add_paragraph(numbered.group(1), style="List Number")
            continue

        # Code blocks (```...```)
        if stripped.startswith("```"):
            continue  # skip fences, content lines handled as normal paragraphs

        # Regular paragraph
        p = doc.add_paragraph(stripped)
        for run in p.runs:
            run.font.size = Pt(11)

    buf = BytesIO()
    doc.save(buf)
    return FormattedReport(
        format="docx",
        content=buf.getvalue(),
        content_type="application/vnd.openxmlformats-officedocument.wordprocessingml.document",
        extension=".docx",
    )
