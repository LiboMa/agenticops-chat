"""Report format converter — Markdown to HTML / PDF / DOCX.

Converts markdown reports to multiple output formats with graceful degradation
when optional dependencies (weasyprint, python-docx) are not installed.

Core dependency: ``markdown`` (pure Python, added to core deps).
Optional: ``weasyprint`` (HTML→PDF), ``python-docx`` (MD→DOCX).
"""

import logging
import re
from dataclasses import dataclass
from datetime import datetime, timezone
from typing import Dict, Optional

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Availability flags — checked once at import time
# ---------------------------------------------------------------------------

try:
    import markdown as _md

    _HAS_MARKDOWN = True
except ImportError:
    _HAS_MARKDOWN = False

try:
    import weasyprint  # noqa: F401

    _HAS_WEASYPRINT = True
except (ImportError, OSError):
    _HAS_WEASYPRINT = False

try:
    import docx  # noqa: F401

    _HAS_DOCX = True
except ImportError:
    _HAS_DOCX = False


# ---------------------------------------------------------------------------
# Data class
# ---------------------------------------------------------------------------


@dataclass
class FormattedReport:
    """A single formatted output of a report."""

    format: str  # "markdown", "html", "pdf", "docx"
    content: bytes  # Raw file bytes
    content_type: str  # MIME type
    extension: str  # ".md", ".html", ".pdf", ".docx"


